local weapon_meta = {}

function weapon_meta:GetWeaponInfoByHash(WeaponHash)
    return self[WeaponHash] or nil
end

function weapon_meta:GetWeaponByName(WeaponName)
    for _, v in next, self or {} do
        if v.name == WeaponName then
            return v
        end
    end
    return nil
end

function weapon_meta:WeaponExists(WeaponHashOrName)
    if type(WeaponHashOrName) == "string" then WeaponHashOrName = joaat(WeaponHashOrName) end
    return self[WeaponHashOrName] ~= nil
end

function weapon_meta:GetWeaponModel(WeaponHash)
    return self[WeaponHash]?.modelName
end

return setmetatable(WeaponList,
    {
        __index = weapon_meta,
        __newindex = function() end,
        __tojson = function() return "" end,
        __tostring = function()
            return ""
        end
    }
)
